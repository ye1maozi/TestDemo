/**
 * 程序化像素画占位图：根据 16x16 点阵生成 SVG，再转为 Data URL
 * 1 = 前景色(tint)，0 = 深色底
 */
const W = 16;
const H = 16;
const BG = "#0f1b33";

function svgDataUrl(grid, fg) {
  const parts = [];
  for (let y = 0; y < H; y++) {
    for (let x = 0; x < W; x++) {
      if (grid[y] && grid[y][x]) parts.push(`<rect x="${x}" y="${y}" width="1" height="1" fill="${fg}"/>`);
    }
  }
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${W} ${H}" width="${W}" height="${H}">` +
    `<rect width="${W}" height="${H}" fill="${BG}"/>` + parts.join("") + "</svg>";
  return "data:image/svg+xml," + encodeURIComponent(svg);
}

